package com.kinancity.captcha.captchaai;

import com.kinancity.core.captcha.CaptchaException;

/**
 * ImageTypers Captcha Errors
 * 
 * @author drallieiv
 *
 */
public class CaptchaAiConfigurationException extends CaptchaException {

	private static final long serialVersionUID = -1173353148853930289L;

	public CaptchaAiConfigurationException() {
		super();
	}

	public CaptchaAiConfigurationException(String message, Throwable cause) {
		super(message, cause);
	}

	public CaptchaAiConfigurationException(String message) {
		super(message);
	}

	public CaptchaAiConfigurationException(Throwable cause) {
		super(cause);
	}

}
